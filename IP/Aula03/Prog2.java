public class Prog2 {
    public static void main(String[] args) {
        //println -> imprime cada dado em um linha
        System.out.println("Inicinado as aulas");
        System.out.println(2024);

        //Imprime tudo na mesma linha
        System.out.print("Inicinado as aulas");
        System.out.print(2024);

        System.out.println("\n");

        //Fazendo o print se comportar como println
        System.out.print("Inicinado as aulas\n");
        System.out.print(2024+"\n");
    }
}
