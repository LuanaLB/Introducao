//Exemplo de como imprimir texto e número na mesma linha
public class Prog4 {
    public static void main(String[] args) {
        //Núemero visto como texto
        System.out.println("Aulas de 2024");
        
        /*Imprimindo texto com número
        "Aulas" + 2024
        "Aulas 2024"
        + significa concatenar
        */
        System.out.println("Aulas de "+ 2024);
    }
}
