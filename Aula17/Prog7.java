//Exemplos de uso do for
public class Prog7 {
    public static void main(String[] args) {
        int x=0;
        for(int i=0, j=0;i <5; i++,j++,x++){
            System.out.println("i = "+i);   
            System.out.println("j = "+j);
            System.out.println("x = "+x);
        }
    }
}
